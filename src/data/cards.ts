export function getCardById(id: string): Card | undefined {
  return cards.find((card) => card.id === id)
}

export function getCardsByCategory(category: string): Card[] {
  if (category === "all") return cards
  return cards.filter((card) => card.category === category)
}

export function getCardsByAgeGroup(ageGroup: string): Card[] {
  if (ageGroup === "all") return cards
  return cards.filter((card) => card.ageGroup === ageGroup)
}

export function getFeaturedCards(ageGroup: string): Card[] {
  return cards.filter((card) => card.isFeatured && card.ageGroup === ageGroup)
}
